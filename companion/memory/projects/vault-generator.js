const fs = require("node:fs");
const path = require("node:path");
const { CANONICAL_PROJECT_FILES } = require("../retrieval/canonical-pages");
const validPolicy = require("../../schemas/fixtures/development-memory/capture-policy.valid.json");
const { DEFAULT_ALLOWED_PATHS, DEFAULT_BLOCKED_PATHS } = require("./project-paths");

const TEMPLATE_ROOT = path.join(__dirname, "..", "..", "..", "templates", "memory-vault");

function copyIfExists(source, target) {
  if (!fs.existsSync(source)) {
    return false;
  }

  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.copyFileSync(source, target);
  return true;
}

function writeFileEnsured(filePath, content) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content, "utf8");
}

function buildCanonicalPageContent(fileName, slug, displayName) {
  const title = fileName.replace(/\.md$/, "").replace(/-/g, " ");

  switch (fileName) {
    case "PROJECT.md":
      return `# ${displayName}\n\nProject slug: \`${slug}\`\n\n## Overview\n\nStarter project page generated by Locaily DM10 setup.\n`;
    case "STATUS.md":
      return `# Status\n\n## Current state\n\n- Project registered in Local Brain.\n- Capture policy pending operator review.\n`;
    case "DECISIONS.md":
      return `# Decisions\n\n## Decisions\n\n- Use canonical project pages for retrieval.\n`;
    case "BLOCKERS.md":
      return `# Blockers\n\n## Open questions\n\n- None recorded yet.\n`;
    case "ARCHITECTURE.md":
      return `# Architecture\n\n## Notes\n\n- Memory Bridge reads allowlisted vault paths only.\n`;
    case "OPERATING-RULES.md":
      return `# Operating Rules\n\n## Constraints\n\n- Writeback remains proposal-only unless explicitly enabled.\n`;
    case "LESSONS.md":
      return `# Lessons\n\n- Prefer canonical pages over raw session logs.\n`;
    case "RESUME.md":
      return `# Resume\n\n## Next steps\n\n- Review generated starter files and enable capture.\n`;
    default:
      return `# ${title}\n\nGenerated starter page for ${displayName}.\n`;
  }
}

function buildCapturePolicy(slug, captureSources = {}) {
  return {
    ...validPolicy,
    enabled: false,
    project: slug,
    capture: {
      ...validPolicy.capture,
      ...captureSources
    },
    writeback: {
      ...validPolicy.writeback
    },
    retention: {
      ...validPolicy.retention
    }
  };
}

function generateStarterVault({
  vaultPath,
  slug,
  displayName,
  layout = "canonical",
  captureSources = {}
}) {
  const warnings = [];
  const targetRoot = path.resolve(vaultPath);

  if (fs.existsSync(targetRoot) && fs.readdirSync(targetRoot).length > 0) {
    return {
      ok: false,
      error: {
        code: "VAULT_NOT_EMPTY",
        message: "Target vault path is not empty.",
        nextStep: "Choose an empty directory or use import-vault for existing vaults."
      },
      warnings: []
    };
  }

  fs.mkdirSync(targetRoot, { recursive: true });

  copyIfExists(path.join(TEMPLATE_ROOT, "index.md"), path.join(targetRoot, "index.md"));
  copyIfExists(path.join(TEMPLATE_ROOT, "log.md"), path.join(targetRoot, "log.md"));
  copyIfExists(path.join(TEMPLATE_ROOT, "SCHEMA.md"), path.join(targetRoot, "SCHEMA.md"));

  const bridgeDir = path.join(targetRoot, ".memory-bridge");
  fs.mkdirSync(path.join(bridgeDir, "writeback-inbox"), { recursive: true });
  fs.mkdirSync(path.join(bridgeDir, "applied"), { recursive: true });
  fs.mkdirSync(path.join(bridgeDir, "rejected"), { recursive: true });

  writeFileEnsured(
    path.join(bridgeDir, "capture-policy.json"),
    `${JSON.stringify(buildCapturePolicy(slug, captureSources), null, 2)}\n`
  );

  writeFileEnsured(
    path.join(bridgeDir, "allowlist.example.json"),
    `${JSON.stringify({ allowedPaths: DEFAULT_ALLOWED_PATHS, blockedPaths: DEFAULT_BLOCKED_PATHS }, null, 2)}\n`
  );

  if (layout === "canonical") {
    const projectDir = path.join(targetRoot, "projects", slug);
    for (const fileName of CANONICAL_PROJECT_FILES) {
      writeFileEnsured(
        path.join(projectDir, fileName),
        buildCanonicalPageContent(fileName, slug, displayName)
      );
    }
  } else {
    writeFileEnsured(
      path.join(targetRoot, "projects", `${displayName}.md`),
      `# ${displayName}\n\nFlat project page.\n`
    );
  }

  fs.mkdirSync(path.join(targetRoot, "topics"), { recursive: true });
  writeFileEnsured(path.join(targetRoot, "topics", "getting-started.md"), `# Getting Started\n\nVault for ${displayName}.\n`);

  if (!fs.existsSync(path.join(targetRoot, "index.md"))) {
    warnings.push("Template index.md was missing; vault may need manual index.");
  }

  return {
    ok: true,
    result: {
      vaultPath: targetRoot,
      slug,
      layout,
      generatedFiles: [
        "index.md",
        "log.md",
        layout === "canonical" ? `projects/${slug}/` : `projects/${displayName}.md`,
        ".memory-bridge/capture-policy.json"
      ]
    },
    warnings
  };
}

module.exports = {
  generateStarterVault,
  buildCapturePolicy
};
